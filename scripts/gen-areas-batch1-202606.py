# ---------- TS serialization ----------
def ts(v, indent=4):
    sp = " " * indent
    if isinstance(v, str):
        return "'" + v.replace("\\", "\\\\").replace("'", "\\'") + "'"
    if isinstance(v, list):
        return "[" + ", ".join(ts(x, indent) for x in v) + "]"
    if isinstance(v, dict):
        items = [f"{k}: {ts(val, indent)}" for k, val in v.items() if val is not None]
        return "{ " + ", ".join(items) + " }"
    return json.dumps(v)

def gym_ts(gym):
    lines = ["  {"]
    for key in ["name", "officialUrl", "price", "trial", "features", "description", "access", "address", "popularPlan", "options", "userProfile", "basicInfo"]:
        v = gym.get(key)
        if v is None or (isinstance(v, list) and not v):
            continue
        lines.append(f"    {key}: {ts(v)},")
    lines.append("  },")
    return "\n".join(lines)


def esc_jsx(s):
    return s.replace("{", "{'{'}").replace("}", "{'}'}")

# ---------- main ----------
def main():
    # 1) pages
    for a in AREAS:
        d = os.path.join(ROOT, "app", "areas", a["slug"])
        os.makedirs(d, exist_ok=True)
        with open(os.path.join(d, "page.tsx"), "w", encoding="utf-8") as f:
            f.write(page_tsx(a))
        print(f"page: {a['slug']} ({len(a['gyms'])} gyms)")

    # 2) areaContextData.ts
    path = os.path.join(ROOT, "components", "areaContextData.ts")
    src = open(path, encoding="utf-8").read()
    additions = []
    for a in AREAS:
        if re.search(rf"^  {a['slug']}:", src, re.M):
            continue
        ch = "".join(f'\n    "{c}",' for c in a["ctx"])
        tp = ", ".join(f'"{t}"' for t in a["tips"])
        additions.append(f'  {a["slug"]}: {{ name: "{a["name"]}", characteristics: [{ch}\n  ], tips: [{tp}] }},')
    if additions:
        src = src.rstrip()
        assert src.endswith("};")
        src = src[:-2] + "\n".join(additions) + "\n};\n"
        open(path, "w", encoding="utf-8").write(src)
    print(f"areaContextData: +{len(additions)}")

    # 3) RelatedAreas.tsx allAreas
    path = os.path.join(ROOT, "components", "RelatedAreas.tsx")
    src = open(path, encoding="utf-8").read()
    added = 0
    for a in AREAS:
        if f"slug: '{a['slug']}'" in src:
            continue
        src = src.replace("]\n", f"  {{ name: '{a['name']}', slug: '{a['slug']}' }},\n]\n", 1)
        added += 1
    open(path, "w", encoding="utf-8").write(src)
    print(f"RelatedAreas: +{added}")

    # 4) sitemap.xml
    path = os.path.join(ROOT, "public", "sitemap.xml")
    src = open(path, encoding="utf-8").read()
    added = 0
    for a in AREAS:
        url = f"https://woman-gym.com/areas/{a['slug']}/"
        if url in src:
            continue
        line = f"  <url><loc>{url}</loc><lastmod>2026-06-12</lastmod><changefreq>weekly</changefreq><priority>0.8</priority></url>\n"
        src = src.replace("</urlset>", line + "</urlset>")
        added += 1
    open(path, "w", encoding="utf-8").write(src)
    print(f"sitemap: +{added}")

if __name__ == "__main__":
    main()
